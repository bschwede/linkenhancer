declare(strict_types=1);

namespace Schwendinger\Webtrees\Module\LinkEnhancer;

use Cissee\WebtreesExt\Module\ModuleExtGlobalInterface;
/**
 * Wrapper class for LinkEnhancerModule in order to also show webtrees manual link on admin backend
 * 
 */
class LinkEnhancerModuleExt extends LinkEnhancerModule implements ModuleExtGlobalInterface {

    /**
     * Raw content, to be added at the end of the <body> element.
     * Typically, this will be <script> elements.
     *
     * @return string
     */
    public function bodyContentOnAdminPage(): string {
        return $this->bodyContent();
    }

    /**
     * Raw content, to be added at the end of the <head> element.
     * Typically, this will be <link> and <meta> elements.
     *
     * @return string
     */
    public function headContentOnAdminPage(): string {
        return $this->headContent();
    }

}